export function buildResultInsightsPrompt(input: { payload: unknown; locale: string }) {
    const { payload, locale } = input;

    return [
        'You rewrite structured data insights into concise user-facing analysis.',
        'Return valid JSON only.',
        `Locale: ${locale}.`,
        'Rules:',
        '- Use only the provided facts and patterns.',
        '- Treat summary, keyColumns, facts, patterns, compact profileColumns, and representative sampleRows as the deterministic fact layer.',
        '- profileColumns is compact and only includes name, semanticRole, distinctCount, topValueShare, and up to 3 topK values.',
        '- sampleRows contains representative rows only, not the full result set.',
        '- Return insights as items. Each item is one independent insight with its own Action buttons.',
        '- Each item must include title, summary, level, primaryAction, and actions. Use an id from the matching fact or pattern when possible.',
        '- level must be one of "primary", "secondary", or "info". Output exactly one primary item.',
        '- Put long-tail amount, outlier, or measure spread insights in primary when present.',
        '- Put status concentration, risk, cancellation/refund proximity, or category concentration insights in secondary.',
        '- Put reliable time column or weak profile-only observations in info, or omit them when they duplicate a stronger time insight.',
        '- Do not return a global recommendedActions list for the UI. Actions belong inside the item they explain.',
        '- If a column has topValueShare 1, do not call the top value an insight. Explain the lack of variance and recommend a better next step.',
        '- For weak raw-row results, prefer a concrete structured action over another explanation.',
        '- Recommend exactly one default button per item as primaryAction. You may keep other candidates in actions, but the UI will only show primaryAction.',
        '- If profileColumns includes usable time, dimension, or measure columns, at least one item should have actions.',
        '- Output at most one primary action per item and at most three actions per item.',
        '- You may provide recommendedSql for the best next action when SQL would be more precise than a simple action template.',
        '- The only allowed item action type values are "filter", "group", "trend", and "distribution".',
        '- If the action is a drilldown, compare, topk, or other free-form action, omit it instead of returning it.',
        '- Use analysis-language titles that describe what the user wants to inspect, not the tool name.',
        '- Do not output statistical values such as p50, p95, maxima, exact ratios, or thresholds in user-facing insight text.',
        '- Do not only describe a profile signal. Every insight must state the conclusion and why it changes the analysis.',
        '- Any recommendedSql must be a single read-only SELECT that only uses columns present in profileColumns and may wrap the current SQL as a subquery.',
        '- Every structured action must use only columns present in profileColumns.',
        '- autoRunPolicy must be confirm_required when recommendedSql or any item action is present.',
        '- Do not invent values, ratios, anomalies, or correlations.',
        '- Do not claim causation.',
        '- Keep insights short, natural, and actionable.',
        '- Produce 3 to 5 items.',
        '',
        'Output JSON shape:',
        '{"quickSummary":{"title":"...","subtitle":"..."},"primaryInsight":"...","items":[{"id":"...","level":"primary","title":"...","summary":"...","primaryAction":{"type":"distribution","title":"...","params":{"column":"amount"},"priority":"primary"},"actions":[]}],"recommendedSql":null,"autoRunPolicy":"confirm_required"}',
        '',
        'Input:',
        JSON.stringify(payload, null, 2),
    ].join('\n');
}
